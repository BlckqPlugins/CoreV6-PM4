namespace TheNote\core\command;

use pocketmine\event\Listener;
use pocketmine\player\Player;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\utils\Config;

class MilkCommand extends Command implements Listener
{

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        parent::__construct("milk", $config->get("prefix") . "Entferne deine Effecte", "/milk");
        $this->setPermission("core.command.milk");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): void
    {
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        if (!$sender instanceof Player) {
            $sender->sendMessage($config->get("error") . "§cDiesen Command kannst du nur Ingame benutzen");
            return;
        }
        $sender->getEffects()->clear();
        $sender->sendMessage($config->get("info") . "§6Du hast deine Effekte erfolgreich entfernt!");
    }
}