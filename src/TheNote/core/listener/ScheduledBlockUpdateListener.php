namespace TheNote\core\listener;

use pocketmine\event\Listener;

use pocketmine\event\world\WorldLoadEvent;
use pocketmine\event\world\WorldSaveEvent;
use TheNote\core\Main;

class ScheduledBlockUpdateListener implements Listener {

    public function onLevelLoad(WorldLoadEvent $event) : void {
        Main::getInstance()->getScheduledBlockUpdateLoader()->loadLevel($event->getWorld());
    }

    public function onLevelSave(WorldSaveEvent $event) : void {
        Main::getInstance()->getScheduledBlockUpdateLoader()->saveLevel($event->getWorld());
    }
}